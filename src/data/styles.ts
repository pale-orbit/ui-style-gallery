export const getStyleById = (id: string): UIStyle | undefined => {
  return styles.find((s) => s.id === id);
};

export const getStylesByCategory = (category: string): UIStyle[] => {
  if (category === 'all') return styles;
  return styles.filter((s) => s.category === category);
};

export const searchStyles = (query: string): UIStyle[] => {
  const q = query.toLowerCase();
  return styles.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.nameEn.toLowerCase().includes(q) ||
      s.description.toLowerCase().includes(q) ||
      s.features.some((f) => f.toLowerCase().includes(q))
  );
};
